# Напишите программу, которая считывает с клавиатуры два числа a и b,
# считает и выводит на консоль среднее арифметическое всех чисел из отрезка
# [a;b], которые кратны числу 3.
# В приведенном ниже примере среднее арифметическое считается для чисел на
# отрезке [−5;12]. Всего чисел, делящихся на 3, на этом отрезке 6:
# -3,0,3,6,9,12. Их среднее арифметическое равно 4.5
# На вход программе подаются интервалы, внутри которых всегда есть хотя бы
# одно число, которое делится на 3.

# a,b = (int(i) for i in input().split())
a = int(input())
b = int(input())

count, u = 0, 0

for x in range(a, b + 1):
    if x % 3 == 0:
        count += 1
        u += x
print(u / count)
